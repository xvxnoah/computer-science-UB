    public void mostrarLlistatProductes(){
        DefaultListModel model = new DefaultListModel();
        model.clear();
        //obtenir iterador sobre ArrayList
        //avan√ßar
        for (Producte p : llistaProductes) {
            model.addElement(p);
        }
        lstProd.setModel(model);
    }
    
